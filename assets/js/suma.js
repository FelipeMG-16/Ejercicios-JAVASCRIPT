//*Crear una función que reciba por parámetros 2 números y los sume.
//*Imprimir por console.log diferentes sumas

//!Declaramos la función sumar, donde debemos especificar los valores que vamos a sumar

function suma(num1, num2) {
  //*Los datos dentro del paréntesis son los dos datos que recibiremos

  return num1 + num2; //* La instrucción return regresa los valores a sumar
}

console.log(suma(3, 8)); //*Mostramos en consola la función "suma", dándole dos valores para sumar
console.log(suma(6, 10));
console.log(suma(2, 8));
console.log(suma(3, 15));
console.log(suma(5, 1));
